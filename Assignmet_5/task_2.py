# Task 2: Demonstrate List Slicing 




# # Create a list of numbers from 1 to 10
originalList = list(range(1, 11))
ExtractedList = originalList[:5]  # Extract the first five elements
ReversedList = ExtractedList[::-1]  # Reverse the extracted elements
print("Original List:", originalList)
print("Extracted First Five Elements:", ExtractedList)
print("Extracted Reversed Elements:", ReversedList)